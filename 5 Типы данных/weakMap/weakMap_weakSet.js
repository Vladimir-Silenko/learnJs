/**
 * Есть массив сообщений такой же, как и в предыдущем задании.
 * Теперь вопрос стоит так:
 *  какую структуру данных вы бы предложили использовать для хранения информации о том,
 *  когда сообщение было прочитано?
 */


//здесь уже потребуется weakMap
let whenHaveBeenRead = new WeakMap()
whenHaveBeenRead.set(messages[0], '22:00')
whenHaveBeenRead.set(messages[2], '12:43')
console.log(whenHaveBeenRead)