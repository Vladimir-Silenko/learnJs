/**
 * Есть массив сообщений:
*/
let messages = [
    { text: "Hello", from: "John" },
    { text: "How goes?", from: "John" },
    { text: "See you soon", from: "Alice" }
]
/**У вас есть к ним доступ, но управление этим массивом происходит где-то ещё.
Добавляются новые сообщения и удаляются старые, и вы не знаете в какой момент
 это может произойти.
Имея такую вводную информацию, решите, какую структуру данных вы могли бы использовать
для ответа на вопрос «было ли сообщение прочитано?».
 */
//в данном случае подойдёт weakSet, 
//так как не требуется сохранять какие-либо дополнительные данные
let beenRead = new WeakSet()
beenRead.add(messages[1])
beenRead.add(messages[2])
console.log(beenRead)
