// Что выведет алерт?
alert(null || 2 || undefined);
// выведет 2 так как оператор "||" возвращает первое истинное значение


// ------------2---------
// Что выведет алерт?
alert(alert(1) || 2 || alert(3));
// сперва выполнит алерт 1, потом вернёт 2,
//  так как алерт не возвращает ничего, а второй операнд == true


// ------------3---------
// Что выведет код ниже?
alert(1 && null && 2);
// null так как '&&' возвращает первое ложное значение


// ------------4---------
// Что выведет код ниже?
alert(alert(1) && alert(2));
//сперва 1 потом undefined



// ------------5---------
// Что выведет код ниже?
alert(null || 2 && 3 || 4);
//сперва выполнится '&&' и получим (null||3||4) после чего выведется 3



