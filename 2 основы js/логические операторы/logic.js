// Напишите условие if для проверки,
// что переменная age находится в диапазоне между 14 и 90 включительно.

let age = 12
if (age >= 14 && age <= 90) console.log("подходит")
else console.log("не подходит")


// -----------7----------
// Напишите условие if для проверки,
//  что значение переменной age НЕ находится в диапазоне 14 и 90 включительно.

// Напишите два варианта:
//  первый с использованием оператора НЕ !, второй – без этого оператора.
if (!(age >= 14 && age <= 90)) console.log("не подходит")
else console.log("подходит")

if (age < 14 || age > 90) console.log("не подходит")
else console.log("подходит")

